<template xmlns:v-slot="http://www.w3.org/1999/XSL/Transform">
  <v-container fluid grid-list-xl>
    <v-layout justify-center wrap>
      <v-flex xs12 md11>
        <v-form ref="form" lazy-validation>
          <material-card
            color="primary"
            title="配置网站基本信息"
            text="完善网站基本信息后，点击保存设置"
          >
            <v-container py-0>
              <v-layout wrap>
                <template v-for="(item, i) in config">
                  <v-flex
                    v-if="item.name != null && !(typeof item.name == 'string')"
                    :key="i"
                    xs12
                    md12
                  >
                    <p>{{ item.description }}</p>
                    <v-switch v-model="item.name"></v-switch>
                  </v-flex>
                  <v-flex v-else :key="i" xs12 md12>
                    <v-text-field
                      v-model="item.name"
                      :rules="titleRules"
                      hint="不能包含空格和特殊字符,不超过40个字符"
                      class="purple-input"
                      :label="item.description"
                    />
                  </v-flex>
                </template>
                <p class="caption text-gray">
                  站点关闭后将不能访问，后台可正常登录
                </p>
                <v-flex xs12 text-center>
                  <v-btn
                    :loading="loading"
                    rounded
                    color="primary"
                    @click="submit()"
                  >
                    保存设置
                  </v-btn>
                </v-flex>
              </v-layout>
            </v-container>
          </material-card>
        </v-form>
      </v-flex>
    </v-layout>
  </v-container>
</template>

<script>
import { getConfig, saveConfig } from '@/api/dict'

export default {
  name: 'Common',
  layout: 'admin',
  data: () => ({
    loading: false,
    titleRules: [
      (v) => !!v || '内容不能为空!',
      (v) => (v && v.length <= 40) || '内容不得超过40个字符'
    ]
  }),

  async asyncData({ app, query, error }) {
    const res = await app.$axios.$request(getConfig())
    let config = {}
    if (res.code === '200') {
      config = res.data.map((c) => {
        if (c.name === 'true' || c.name === 'false') {
          // eslint-disable-next-line no-eval
          c.name = eval(c.name.toLowerCase())
        }
        return c
      })
    } else {
      return error({
        statusCode: res.code,
        message: res.message
      })
    }
    return {
      config
    }
  },
  methods: {
    submit() {
      this.loading = true
      if (this.$refs.form.validate()) {
        // 校验通过则提交保存
        this.save()
      } else {
        this.loading = false
      }
    },
    save() {
      this.$axios
        .$request(saveConfig(this.config))
        .then((res) => {
          this.loading = false
          if (res.code === '200' && res.data) {
            this.$swal({
              text: '保存成功',
              type: 'success',
              toast: true,
              position: 'top',
              showConfirmButton: false,
              timer: 3000
            })
          } else {
            this.$swal({
              text: res.message,
              type: 'error',
              toast: true,
              position: 'top',
              showConfirmButton: false,
              timer: 3000
            })
          }
        })
        .catch((e) => {
          this.loading = false
          this.$swal({
            text: e.message,
            type: 'error',
            toast: true,
            position: 'top',
            showConfirmButton: false,
            timer: 3000
          })
        })
    }
  }
}
</script>
